# 모든 요소 합치기
성씨들 = ["김", "이", "박", "최", "정", "강", "조"]
이름들 = ["철수", "영희", "민수", "지은", "현우", "서연", "도현"]
성격들 = ["활발한", "조용한", "유머러스한", "진지한", "친절한"]
취미들 = ["게임", "음악감상", "독서", "운동", "요리"]
음식들 = ["피자", "치킨", "라면", "초밥", "햄버거"]

# 랜덤 생성
이름 = random.choice(성씨들) + random.choice(이름들)
나이 = random.randint(15, 18)
키 = random.randint(150, 190)
몸무게 = random.randint(40, 90)
성격 = random.choice(성격들)
취미 = random.choice(취미들)
좋아하는음식 = random.choice(음식들)

# 멋진 프로필 출력
print(f"📋 이름: {이름}")
print(f"🎂 나이: {나이}세")
print(f"📏 키: {키}cm, 몸무게: {몸무게}kg")
print(f"😊 성격: {성격}")
print(f"🎮 취미: {취미}")
print(f"🍕 좋아하는 음식: {좋아하는음식}")
print("=" * 35)


